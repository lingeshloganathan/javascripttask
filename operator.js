let a = 10;
let b = 5;

//arithemetic operator
console.log(a + b); 
console.log(a - b);  
console.log(a * b);  
console.log(a / b); 
console.log(a % b); 


//assignment operator
let c= 10 
console.log(c += 5) 
console.log(c -= 3)
console.log(c *= 2)  
console.log(c /= 4) 


//comparison operator
console.log(a == b)  
console.log(a === b) 
console.log(a != b)
console.log(a > b)  
console.log(a < b) 
console.log(a >= b) 
console.log(a <= b) 


//logical operator
let x = true
let y = false

console.log(x && y)
console.log(x || y)
console.log(!x)


