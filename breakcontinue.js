//break statement
let i
for (i = 0; i < 10; i++) {
    if (i == 5) {
        break     
    }
    console.log(i)
}
console.log('---------------------------------')


//continue statement
let j
for (j = 0; j < 10; j++) {
    if (j == 5) {
        continue
    }
    console.log(j)
}
